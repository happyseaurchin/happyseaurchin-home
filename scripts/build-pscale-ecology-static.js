#!/usr/bin/env node
/**
 * build-pscale-ecology-static.js
 *
 * pscale-ecology/index.html renders all its content client-side from the
 * OVERVIEW / BRANCHES / INFO_HTML data in its inline <script>. That means a
 * non-JS reader (LLM fetchers, link-preview bots, plain crawlers, curl) sees
 * an empty #column and only the <meta> description.
 *
 * This script reads that same data (single source of truth), renders it to
 * static, readable HTML, and writes it INTO #column between markers. Browsers
 * run the JS and overwrite #column instantly (progressive enhancement); anything
 * that does NOT run JS now gets the full article text.
 *
 * Re-run after editing OVERVIEW / BRANCHES / INFO_HTML:
 *   node scripts/build-pscale-ecology-static.js
 */
const fs = require('fs');
const path = require('path');

const FILE = path.join(__dirname, '..', 'pscale-ecology', 'index.html');
const START = '<!-- STATIC:START — generated by scripts/build-pscale-ecology-static.js · do not hand-edit -->';
const END = '<!-- STATIC:END -->';

let html = fs.readFileSync(FILE, 'utf8');

// ── extract a bracket-delimited literal ([..] or {..}), string/template aware ──
function sliceBracketed(src, declRe, open, close) {
  const m = src.match(declRe);
  if (!m) throw new Error('not found: ' + declRe);
  let i = src.indexOf(open, m.index + m[0].length);
  const start = i;
  let depth = 0, q = null, esc = false;
  for (; i < src.length; i++) {
    const c = src[i];
    if (esc) { esc = false; continue; }
    if (q) { if (c === '\\') esc = true; else if (c === q) q = null; continue; }
    if (c === "'" || c === '"' || c === '`') { q = c; continue; }
    if (c === open) depth++;
    else if (c === close) { depth--; if (depth === 0) return src.slice(start, i + 1); }
  }
  throw new Error('unbalanced for ' + declRe);
}
// ── extract a top-level `template` string literal ──
function sliceTemplate(src, declRe) {
  const m = src.match(declRe);
  let i = src.indexOf('`', m.index + m[0].length);
  const start = i;
  for (i = i + 1; i < src.length; i++) { if (src[i] === '\\') { i++; continue; } if (src[i] === '`') return src.slice(start, i + 1); }
  throw new Error('unterminated template for ' + declRe);
}

const overviewLit = sliceBracketed(html, /const\s+OVERVIEW\s*=\s*/, '[', ']');
const branchesLit = sliceBracketed(html, /const\s+BRANCHES\s*=\s*/, '[', ']');
const railLit = sliceBracketed(html, /const\s+RAIL_ORDER\s*=\s*/, '[', ']');
const infoLit = sliceTemplate(html, /const\s+INFO_HTML\s*=\s*/);

// eval as pure data (BIOME_CFG/BSP_CFG referenced by connect frames → stub)
const data = new Function(
  'const BIOME_CFG="";const BSP_CFG="";' +
  'return {OVERVIEW:' + overviewLit + ',BRANCHES:' + branchesLit +
  ',RAIL_ORDER:' + railLit + ',INFO_HTML:' + infoLit + '};'
)();

const esc = s => String(s == null ? '' : s).replace(/[&<>]/g, c => ({ '&': '&amp;', '<': '&lt;', '>': '&gt;' }[c]));
const stripSvg = s => String(s).replace(/<svg[\s\S]*?<\/svg>/g, '');
const BY_ID = Object.fromEntries(data.BRANCHES.map(b => [b.id, b]));

function renderFrame(f) {
  if (f.kind === 'fork') return '';
  let h = '';
  if (f.title) h += `<h4>${esc(f.title)}</h4>\n`;
  if (f.body) h += stripSvg(f.body).trim() + '\n';
  if (f.note) h += `<p class="ssr-note">${f.note}</p>\n`;
  if (f.kind === 'embed' && (f.open || f.src)) h += `<p>Live tool: <a href="${esc(f.open || f.src)}">${esc(f.open || f.src)}</a></p>\n`;
  if (f.kind === 'connect') {
    const ep = f.endpoint || 'https://bsp.hermitcrab.me/mcp/v1';
    h += `<p>Connect via ${esc(f.via || 'bsp-mcp')} at <a href="${esc(ep)}">${esc(ep)}</a>.</p>\n`;
  }
  if (Array.isArray(f.cards) && f.cards.length) {
    h += '<ul>\n' + f.cards.map(c => `<li><a href="${esc(c.href)}">${esc(c.title)}</a> — ${esc(c.desc)}</li>`).join('\n') + '\n</ul>\n';
  }
  return h;
}

function renderBranch(b) {
  let h = `<section data-path="${esc(b.id)}">\n<h3>${esc(b.title)}</h3>\n<p>${esc(b.lede)}</p>\n`;
  h += b.frames.map(renderFrame).join('');
  return h + '</section>\n';
}

let body = `<article id="ssr-fallback">\n`;
body += `<h1>The pscale ecology</h1>\n<p>An LLM-native substrate. ${esc('The pscale block, the function that reads it, the beach it lives on — and the use-cases that grow from there.')}</p>\n`;

// overview cards (claim + systemic comparison)
body += `<section id="ssr-overview">\n<h2>Overview</h2>\n`;
for (const c of data.OVERVIEW) {
  body += `<h3>${esc(c.label)} — ${esc(c.sub)}</h3>\n${c.front.trim()}\n<p><strong>What others do instead:</strong></p>\n${c.back.trim()}\n`;
}
body += `<h3>Why this exists — two hidden attractors</h3>\n${data.INFO_HTML.trim()}\n</section>\n`;

// full segmented paths, in rail order
body += `<section id="ssr-paths">\n<h2>The paths</h2>\n`;
for (const id of data.RAIL_ORDER) { if (BY_ID[id]) body += renderBranch(BY_ID[id]); }
body += `</section>\n</article>`;

const block = START + '\n' + body + '\n' + END;

if (html.includes(START)) {
  const a = html.indexOf(START), z = html.indexOf(END) + END.length;
  html = html.slice(0, a) + block + html.slice(z);
} else {
  if (!html.includes('<div id="column"></div>')) throw new Error('cannot find empty #column to seed');
  html = html.replace('<div id="column"></div>', '<div id="column">' + block + '</div>');
}

fs.writeFileSync(FILE, html);
const chars = body.replace(/<[^>]+>/g, ' ').replace(/\s+/g, ' ').trim().length;
console.log(`OK — wrote static fallback: ${data.OVERVIEW.length} overview cards, ${data.RAIL_ORDER.length} paths, ~${chars} chars of readable text.`);
